/*
*
* La clase "principal", no es más que una clase que contiene el <<método principal>>,
* y así es como la clase se vuelve la/una clase principal.
*
*/
 public class ClasePrincipal {

  /*
  *
  * Este es el método principal, y su estructura es así:
  * <<public static void main(String[] NOMBRE_DEL_LA_VARIABLE_PARAMETRO){}>>
  * de no escribirse así el compilador no considerará la clase como principal
  * cuando intentemos ejecutarla con el comando "java"
  *
  */
   public static void main(String[] parametro){
    // intrucciones que se ejecutaran en el método principal

    // Aquí haremos una impresión en consola con el texto "Hola Mundo"
    System.out.println("Hola Mundo");
  }
}
